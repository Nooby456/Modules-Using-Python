import math

def squareroot (number):
    x = math.sqrt(number)
    return (x)

def add (number1, number2):
    x = number1 + number2 
    return (x)

def subtract (number1, number2):
    x = number1 - number2 
    return (x)

def multiply (number1, number2):
    x = number1 * number2 
    return (x)

def divide (number1, number2):
    x = number1 / number2 
    return (x)
